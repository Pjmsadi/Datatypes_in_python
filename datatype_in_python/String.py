# Strings represent text and are written in single or double quotes.

# String Example

a = "Hello"
b = 'World'
c = '''Hello, World!'''
print(a, b, c)
# Output
#  Hello World Hello, World!
# In the above example, a, b, and c are string variables. a is "Hello", b is 'World', and c is '''Hello, World!'''.
